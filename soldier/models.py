        СТАРШИЙ_ПРАПОРЩИК = "Старший прапорщик", "Старший прапорщик"
        МЛАДШИЙ_ЛЕЙТЕНАНТ = "Младший лейтенант", "Младший лейтенант"
        ЛЕЙТЕНАНТ = "Лейтенант", "Лейтенант"
