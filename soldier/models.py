    first_name = models.CharField(
        max_length=50, verbose_name="Имя")

    last_name = models.CharField(
        max_length=50, verbose_name="Фамилия")

    patronymic = models.CharField(
        max_length=50, verbose_name="Отчество", default='', blank=True, null=True)

    birthday = models.DateField(
        blank=True, null=True, verbose_name="Дата рождения")

    military_id = models.CharField(
        max_length=50, blank=True, null=True, verbose_name="Номер военного билета")

    rank = models.CharField(
        max_length=50,
        choices=Rank.choices,
        default=Rank.РЯДОВОЙ,
        verbose_name="Звание",
        blank=True,
        null=True
    )

    unit = models.CharField(
        max_length=100,
        choices=Unit.choices,
        default=Unit.МОТОСТРЕЛКОВЫЕ_ВОЙСКА,
        verbose_name="Подразделение",
        blank=True,
        null=True
    )


    def __str__(self):
        return f"{self.rank} {self.last_name} {self.first_name}"

